void	ft_rev_int_tab(int *tab, int size)
{
	int	tab2[size];
	int	i;
	int	s;

	s = size;
	i = 0;
	while (i < size)
	{
		tab2[s - 1] = tab[i];
		i++;
		s--;
	}
	i = 0;
	while (i < size)
	{
		tab[i] = tab2[i];
		i++;
	}
}
